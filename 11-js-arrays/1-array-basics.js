//Array is collection of vars
var myarray = ["Hello", "World"];

//Access with square brackets - 0 indexed
console.log(myarray[0]);

//Can add as desired
myarray[2] = "New string";
console.log(myarray[2]);

//Instantiation
myarray = [];
myarray = new Array();

//Any type of data
myarray = [1, 1.7, "bananas"];

//can nest
myarray = [["11", "12"],["21", "22"]];
